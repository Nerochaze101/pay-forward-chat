interface SocialShareButtonsProps {
  shareText: string;
  shareUrl: string;
  onImageShare?: () => Promise<void>;
  onInstagramClick?: () => void;
}

export function SocialShareButtons({ shareText, shareUrl, onImageShare, onInstagramClick }: SocialShareButtonsProps) {
  const handleWhatsApp = async () => {
    if (onImageShare) {
      await onImageShare();
    } else {
      window.open(`https://wa.me/?text=${encodeURIComponent(shareText)}`, "_blank");
    }
  };

  const handleShare = async () => {
    if (onImageShare) {
      await onImageShare();
    }
  };

  const buttons = [
    { icon: WhatsAppIcon, label: "WhatsApp", color: "#25D366", onClick: handleShare },
    { icon: TwitterXIcon, label: "X", color: "#ffffff", onClick: handleShare },
    { icon: InstagramIcon, label: "Instagram", color: "#E4405F", onClick: handleShare },
    { icon: SnapchatIcon, label: "Snapchat", color: "#FFFC00", onClick: handleShare },
    { icon: FacebookIcon, label: "Facebook", color: "#1877F2", onClick: handleShare },
    { icon: TelegramIcon, label: "Telegram", color: "#26A5E4", onClick: handleShare },
  ];

  return (
    <div className="flex gap-3 flex-wrap">
      {buttons.map((b) => (
        <button
          key={b.label}
          onClick={b.onClick}
          className="group flex items-center gap-2 px-4 py-2.5 rounded-xl bg-secondary/80 hover:bg-secondary border border-border hover:border-primary/30 transition-all duration-200 hover:scale-105"
          title={b.label}
        >
          <b.icon className="w-5 h-5" />
          <span className="text-xs font-medium text-muted-foreground group-hover:text-foreground transition-colors">{b.label}</span>
        </button>
      ))}
    </div>
  );
}
